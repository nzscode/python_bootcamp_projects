def add(a, b):
    if type(a) != int or type(b) != int:
        return "Not valid numbers"
    return a+b

def multiply(a, b):
    if type(a) != int or type(b) != int:
        return "Not valid numbers"
    return a*b

def divide(a, b):
    if type(a) != int or type(b) != int:
        return "Not valid numbers"
    return a/b

def subtract(a, b):
    if type(a) != int or type(b) != int:
        return "Not valid numbers"
    return a-b
